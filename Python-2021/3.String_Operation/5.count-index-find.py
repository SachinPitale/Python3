
# count method: it will give the total number of count of char and words

x="python is a easy and it is popular language"
print (x.count('is'))
print (x.count('p'))
print (x.count('a'))


# index method : it will give us index number of char 

print (x.index('p'))
print (x.index('p',2)) # it will find after 2nd index
print (x.index('is'))  
print (x.index('is',8))


# find method : get the index number of char or word

print (x.find('a'))
print (x.find('popular'))
